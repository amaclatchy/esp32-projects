void set_car(uint8_t *cars, int position, uint8_t flag, uint8_t *buffer, uint8_t *save) {
	int index = 0;
	for(int page=6;page<8;page++) {
		for(int seg=0;seg<16;seg++) {
			if (save) save[index] = buffer[page*128+seg+position];
			if (position < 0) continue;
			if (page == 6) {
				uint8_t _flag = flag & 0x01; 
				if (_flag == 0x01) 
					buffer[page*128+seg+position] = cars[index];
			} else {
				uint8_t _flag = flag & 0x02; 
				if (_flag == 0x02) 
					buffer[page*128+seg+position] = cars[index];
			}
			index++;
		}
	}
}

